import { createLauncherProfileController } from "../../profile/launcher-profile-controller.js";

export function createLauncherProfileDomain({
  accountDataService,
  accountUi,
  auth,
  currentSeason,
  identity,
  messages,
  profile,
  profileLoaderService,
  profileModalState,
  profileSeason,
  ranking,
  sharedState,
  t,
  launcherRankingSync,
  launcherMessageOrchestrator,
  getAuthStateController
}) {
  return createLauncherProfileController({
    accountDataService,
    applyNickname: identity.applyNickname,
    claimAccountMessageReward: profile.claimAccountMessageReward,
    currentSeason,
    formatNumber: accountUi.formatNumber,
    getAuthErrorMessage: auth.getAuthErrorMessage,
    getAuthNicknameCheckFailedMessage: auth.getAuthNicknameCheckFailedMessage,
    getAuthNicknameRequiredMessage: auth.getAuthNicknameRequiredMessage,
    getAuthNicknameTakenMessage: auth.getAuthNicknameTakenMessage,
    getMemberDisplayName: identity.getMemberDisplayName,
    getMessagesClaimFailedText: messages.getMessagesClaimFailedText,
    getMessagesClaimedText: messages.getMessagesClaimedText,
    getMessagesFailedText: messages.getMessagesFailedText,
    getMessagesLoadingText: messages.getMessagesLoadingText,
    getProfileDisplayName: identity.getProfileDisplayName,
    getProfileFailedMessage: profile.getProfileFailedMessage,
    getProfileLoadingMessage: profile.getProfileLoadingMessage,
    getProfileNicknameSavedMessage: profile.getProfileNicknameSavedMessage,
    getProfileNicknameUnchangedMessage: profile.getProfileNicknameUnchangedMessage,
    getProfilePartialFailedMessage: profile.getProfilePartialFailedMessage,
    getRankingSeasonConfig: ranking.getRankingSeasonConfig,
    getRequestedProfileNickname: profile.getRequestedProfileNickname,
    isAccountMessageClaimEnabled: profile.isAccountMessageClaimEnabled,
    isAccountMessageInboxEnabled: messages.isAccountMessageInboxEnabled,
    isAccountWalletEnabled: accountUi.isAccountWalletEnabled,
    isNicknameAvailable: profile.isNicknameAvailable,
    isPlaytestActive: profile.isPlaytestActive,
    markMessagesSeen: (...args) => launcherMessageOrchestrator.markMessagesSeen(...args),
    markRewardMessagesAlerted: (...args) => launcherMessageOrchestrator.markRewardMessagesAlerted(...args),
    maybeOpenLobbyRewardMessagePrompt: (...args) => launcherMessageOrchestrator.maybeOpenLobbyRewardMessagePrompt(...args),
    normalizeName: identity.normalizeName,
    handleSignOut: auth.handleSignOut,
    profileLoaderService,
    profileModalState,
    profileSeason,
    refreshCurrentSeasonRank: () => getAuthStateController()?.refreshCurrentSeasonRank?.(),
    renderMessages: messages.renderMessages,
    renderProfileSeasonRecord: profile.renderProfileSeasonRecord,
    renderProfileSeasonTopRankings: profile.renderProfileSeasonTopRankings,
    renderProfileSummary: profile.renderProfileSummary,
    resetMessageNotifications: () => launcherMessageOrchestrator.resetMessageNotifications(),
    setMessagesStatus: messages.setMessagesStatus,
    setProfileNicknameBusy: profile.setProfileNicknameBusy,
    setProfileNicknameEditing: profile.setProfileNicknameEditing,
    setProfileStatus: profile.setProfileStatus,
    state: sharedState,
    syncAuthenticatedRankingNicknames: (...args) => launcherRankingSync.syncAuthenticatedRankingNicknames(...args),
    t,
    updateAccountNickname: profile.updateAccountNickname,
    updateAuthUi: () => getAuthStateController()?.updateAuthUi?.(),
    updateCurrentUserNickname: profile.updateCurrentUserNickname,
    updateLobbyPlayerInfo: () => getAuthStateController()?.updateLobbyPlayerInfo?.(),
    updateMessageNotificationState: (...args) => launcherMessageOrchestrator.updateMessageNotificationState(...args),
    updateMessagesButtonLabel: () => launcherMessageOrchestrator.updateMessagesButtonLabel()
  });
}
