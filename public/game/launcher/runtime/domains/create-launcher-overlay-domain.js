import { createLauncherOverlayController } from "../../overlay/launcher-overlay-controller.js";

export function createLauncherOverlayDomain({
  accountUi,
  auth,
  authUi,
  messages,
  messageOverlay,
  profile,
  routing,
  launcherRankingSync,
  launcherMessageOrchestrator,
  getProfileController
}) {
  return createLauncherOverlayController({
    authMinPasswordLength: auth.authMinPasswordLength,
    authModalState: auth.authModalState,
    applySelectedEmailDomain: auth.applySelectedEmailDomain,
    canOpenMessagesInbox: messages.canOpenMessagesInbox,
    clearAuthStatusIfIdle: auth.clearAuthStatusIfIdle,
    clearAuthValidation: auth.clearAuthValidation,
    closeLobbyRewardMessagePrompt: (...args) => launcherMessageOrchestrator.closeLobbyRewardMessagePrompt(...args),
    closeMessagesModal: messageOverlay.closeMessagesModal,
    closeProfileModal: messageOverlay.closeProfileModal,
    closeSettingsModal: authUi.closeSettingsModal,
    closeAuthModal: authUi.closeAuthModal,
    ensureMusicEnabled: accountUi.ensureMusicEnabled,
    getAuthErrorMessage: auth.getAuthErrorMessage,
    getAuthNicknameCheckFailedMessage: auth.getAuthNicknameCheckFailedMessage,
    getAuthNicknameNeedsCheckMessage: auth.getAuthNicknameNeedsCheckMessage,
    getAuthNicknameRequiredMessage: auth.getAuthNicknameRequiredMessage,
    getAuthNicknameTakenMessage: auth.getAuthNicknameTakenMessage,
    getPasswordMismatchMessage: auth.getPasswordMismatchMessage,
    getPasswordTooShortMessage: auth.getPasswordTooShortMessage,
    getRequestedAuthNickname: auth.getRequestedAuthNickname,
    getResetAuthErrorMessage: auth.getResetAuthErrorMessage,
    getResetPasswordPromptMessage: auth.getResetPasswordPromptMessage,
    getResetPasswordSentMessage: auth.getResetPasswordSentMessage,
    handleSignOut: auth.handleSignOut,
    isLoggedIn: auth.isLoggedIn,
    isNicknameAvailable: profile.isNicknameAvailable,
    loadMessagesInbox: (...args) => getProfileController()?.loadMessagesInbox?.(...args),
    loadProfileInfo: (...args) => getProfileController()?.loadProfileInfo?.(...args),
    openAuthModal: authUi.openAuthModal,
    openMessagesModal: messageOverlay.openMessagesModal,
    openProfileModal: messageOverlay.openProfileModal,
    openSettingsModal: authUi.openSettingsModal,
    playLobbyMusic: accountUi.playLobbyMusic,
    resetAuthForm: auth.resetAuthForm,
    resetAuthNicknameCheck: auth.resetAuthNicknameCheck,
    runAuthNicknameCheck: auth.runAuthNicknameCheck,
    sendPasswordResetLink: auth.sendPasswordResetLink,
    setAuthMode: auth.setAuthMode,
    setAuthNicknameStatus: auth.setAuthNicknameStatus,
    setAuthStatus: auth.setAuthStatus,
    setAuthSubmitState: auth.setAuthSubmitState,
    setPasswordVisibility: auth.setPasswordVisibility,
    signInWithEmail: auth.signInWithEmail,
    signUpWithEmail: auth.signUpWithEmail,
    syncAuthenticatedAccount: (...args) => launcherRankingSync.syncAuthenticatedAccount(...args),
    syncEmailDomainSelect: auth.syncEmailDomainSelect,
    syncMobileNavigationState: routing.syncMobileNavigationState
  });
}
